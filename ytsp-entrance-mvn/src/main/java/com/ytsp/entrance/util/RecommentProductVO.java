package com.ytsp.entrance.util;

public class RecommentProductVO {
	public String productName;
	public Integer productCode;
	public String imgSrc;
	public Double marketPrice;
	public Double salePrice;
	public Integer saleNum;
	public Integer quarterSaleNum;
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public Integer getProductCode() {
		return productCode;
	}
	public void setProductCode(Integer productCode) {
		this.productCode = productCode;
	}
	public String getImgSrc() {
		return imgSrc;
	}
	public void setImgSrc(String imgSrc) {
		this.imgSrc = imgSrc;
	}
	public Double getMarketPrice() {
		return marketPrice;
	}
	public void setMarketPrice(Double marketPrice) {
		this.marketPrice = marketPrice;
	}
	public Double getSalePrice() {
		return salePrice;
	}
	public void setSalePrice(Double salePrice) {
		this.salePrice = salePrice;
	}
	public Integer getSaleNum() {
		return saleNum;
	}
	public void setSaleNum(Integer saleNum) {
		this.saleNum = saleNum;
	}
	public Integer getQuarterSaleNum() {
		return quarterSaleNum;
	}
	public void setQuarterSaleNum(Integer quarterSaleNum) {
		this.quarterSaleNum = quarterSaleNum;
	}
	
}
